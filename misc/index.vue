                let app = new Vue({
                  el: "#app",
                  components: {},
                  data() {
                    return {
                      ghSource: "",
                      allJobDepartments: [],
                      allLocations: [],
                      selectedJobDepartment: null,
                      selectedJobLocation: "all",
                      filteredJobDepartments: [],
                      searchInput: "",
                    };
                  },
                  computed: {
                    sourceParameter() {
                      if (this.ghSource == "") {
                        return "";
                      }
                      return `?gh_src=${this.ghSource}`;
                    },
                  },

                  async mounted() {
                    try {
                      var urlParams = new URLSearchParams(
                        window.location.search
                      );
                      this.ghSource = urlParams.getAll("gh_src");

                      const response = await axios.get(
                        "https://boards-api.greenhouse.io/v1/boards/goodhumanptyltd/departments"
                      );

                      //Filter jobs with certain patterns in the name and remove departments with no jobs
                      this.allJobDepartments = response.data.departments
                        .map((department) => {
                          department.jobs = department.jobs.filter(
                            (job) =>
                              !job.title.includes("TIP:") &&
                              !job.title.includes("DNU") &&
                              !job.title.includes("Contract:")
                          );
                          return department;
                        })
                        .filter((department) => department.jobs.length > 0);

                      this.selectedJobDepartment = "all";

                      let locations = new Set();

                      this.allJobDepartments.forEach((department) => {
                        department.jobs.forEach((job) => {
                          locations.add(job.location.name);
                        });
                      });

                      this.allLocations = Array.from(locations).sort();
                    } catch (err) {
                      console.log(err);
                    }
                  },
                  watch: {
                    selectedJobDepartment(departmentId) {
                      if (departmentId === "all") {
                        this.filteredJobDepartments = this.allJobDepartments;
                        return;
                      }

                      this.applyFilters();
                    },
                    selectedJobLocation(location) {
                      if (location === "all") {
                        this.filteredJobDepartments = this.allJobDepartments;
                        return;
                      }

                      this.applyFilters();
                    },
                    searchInput() {
                      this.applyFilters();
                    },
                  },
                  methods: {
                    applyFilters() {
                      this.filteredJobDepartments = this.allJobDepartments
                        .map((department) => {
                          let jobs = department.jobs.filter((job) => {
                            let title = job.title.toUpperCase();
                            let location = job.location.name.toUpperCase();
                            let searchInput = this.searchInput.toUpperCase();

                            return !searchInput || title.includes(searchInput);
                          });

                          if (this.selectedJobLocation !== "all") {
                            jobs = department.jobs.filter(
                              (job) =>
                                job.location.name === this.selectedJobLocation
                            );
                          }

                          if (
                            this.selectedJobDepartment !== "all" &&
                            department.id !== this.selectedJobDepartment
                          ) {
                            jobs = [];
                          }

                          return {
                            id: department.id,
                            name: department.name,
                            jobs: jobs,
                          };
                        })
                        .filter((department) => department.jobs.length > 0);
                    },
                  },
